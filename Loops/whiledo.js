/*
Print 1 to 5
*/

console.log("While Loop ")
let i=1;
while(i<=5){

    console.log(i)
    i++
}


/*
do while
The block executes first

Then the condition is checked

So it runs at least one time, even if condition is false
*/


console.log("do While")

let j=1;
do{
    console.log(j)
    j++

} while(j<=5)