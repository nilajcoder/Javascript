

/**
 * 
 * What is Object
 * 
 * Ans- Object is a data type in JavaScript that allows you
 *  to store collections of data as key-value pairs.
 */

let ball={
    color:"red",
    shape:"round",
    size:"large",
    name:"soccer ball",
    price:20

}
// Accessing object properties
console.log(ball);
console.log(ball.color);//red
console.log(ball.size);//round
console.log(ball.price);//20
console.log(ball.name);//soccer ball


let person={
    firstName:"John",
    lastName:"Doe",
    age:30,
    occupation:"Developer",
    isEmployed:true
}
console.log(person);
console.log(person.firstName);
console.log(person.age);
console.log(person.occupation);
console.log(person.isEmployed);