
// Arrow function
const add=(a,b)=>{
    return a+b;
}
console.log("Addition is "+" "+add(2,3));

// Function declaration
function add1(c,d){
    return c+d;
}
console.log("Addition is "+" "+add1(5,3));

// Function expression
const add2=function(x,y){
    return x+y;
}
console.log("Addition is "+" "+add2(2,2));